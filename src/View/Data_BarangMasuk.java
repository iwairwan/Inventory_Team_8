    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        int row = tabel.getSelectedRow();

        msk.setNo_masuk         (tabel.getModel().getValueAt(row, 0).toString());
        msk.setTgl_masuk        (tabel.getModel().getValueAt(row, 1).toString());
        msk.setTotal_masuk      (Long.parseLong(tabel.getModel().getValueAt(row, 2).toString()));
        dst.setId_distributor   (tabel.getModel().getValueAt(row, 3).toString());
        pgn.setId_pengguna      (tabel.getModel().getValueAt(row, 4).toString());
        
        dispose();
    }//GEN-LAST:event_tabelMouseClicked

